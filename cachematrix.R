## mackCacheMatrix takes a matrix, "x" and stores it in the parent environment
## (rewritting over any previously stored matrices in "x")

makeCacheMatrix <- function(x = matrix()) {
        m <- NULL
        set <- function(y) {
                x <<- y
                m <<- NULL
        }
        get <- function() x
        setsolve <- function(solve) m <<- solve
        getsolve <- function() m
        list(set = set, get = get,
             setsolve = setsolve,
             getsolve = getsolve)
        
}


## cacheSolve takes input "x" and checks to see if the inverse has been 
## calculated. If not, it calculates the inverse. If it has been calculated
## cacheSolve skips the calculation and returns the previously stored result

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
        m <- x$getsolve()
        if(!is.null(m)) {
                message("getting cached data")
                return(m)
        }
        data <- x$get()
        m <- solve(data, ...)
        x$setsolve(m)
        m
}
